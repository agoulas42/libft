#include "../includes/libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*new;

	if (s == NULL || f == NULL)
		return (NULL);
	if (s && f)
	{
		i = 0;
		if ((new = ft_memalloc(ft_strlen(s) + 1)) == NULL)
			return (NULL);
		while (s[i])
		{
			new[i] = f(i, s[i]);
			i++;
		}
		new[i] = '\0';
		return (new);
	}
	return (NULL);
}
